"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

#Aspas simples
print('Flávio Ponciano')

#Aspas duplas
print("Flávio Ponciano")

#Caracter Escape: Adiciona simbolos que são reconhecidos para outras funções no python
print('Flávio \'Ponciano\'') #Nesse caso aspas simples são mostradas na saída do programa

# r -> utilizado em expressões regulares, mas nesse caso é para mostrar a barra
print(r"Flávio \"Ponciano\"")

#A forma mais simples de fazer mostrar as aspas é inventer as aspas usada:
print(1, 'Flávio "Ponciano"')