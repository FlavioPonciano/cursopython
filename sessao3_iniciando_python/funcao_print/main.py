#utilizando print com , para mostrar vários argumentos e quebra linha automaticamente caso for colocado um print abaixo do outro
#sep é um sepador alterando a virgula por outro simbolo
#end adiciona algo ao final do print
print(12, 34, 1011, sep="-", end='##\r\n') 
print(56, 78, sep='_') 

#Quebra de linha
#\r\n -> CRLF
#\n -> LF